using gAPI.AutoSseServer.Models;
using System.Linq;

namespace gAPI.AutoSseServer.Generators;

public class IClientContext_Generator : _BaseGenerator
{
    public IClientContext_Generator(
        Generator context)
    {
        Context = context;

        Directory = "";
        Namespace = "gAPI.Generated";

        Name = "IClientContext";
        FileName = $"{Name}.g.cs";
    }

    public Generator Context { get; }
    public ClientServiceContext_Generator[] ClientHandlerContexts => Context.ClientContexts;

    public override void GenerateCode()
    {
        var properties = string.Join(
            "\r\n",
            ClientHandlerContexts
                .Select(a =>
                {
                    Reg(a.IClientContext);
                    return $"    {a.IClientContext.Name} {a.SseService.Interface.Title} {{ get; }}";
                }));
        Code = @$"{GetNamespacesCode()}
// <auto-generated />
#nullable enable

namespace {Namespace};

public interface {Name}
{{
{properties}
}}
";
    }
}