using gAPI.AutoWssServer.Models;

namespace gAPI.AutoWssServer.Generators;

public class IClientServiceContext_Generator : _BaseGenerator
{
    public IClientServiceContext_Generator(
        Generator context,
        ClientService_Generator clientHandler)
    {
        Context = context;
        ClientHandler = clientHandler;

        Directory = "";
        Namespace = clientHandler.Namespace;

        Name = "I" + ClientHandler.Interface.CleanName + "Context";
        FileName = $"{Name}.g.cs";
    }

    public Generator Context { get; }
    public ClientService_Generator ClientHandler { get; }
    public Interface IClientHandler => ClientHandler.Interface;
    public WssHub_Generator WssHub => Context.WssHub;
    public SharedReference UserId => Context.SharedReferences.UserId;
    public SharedReference SessionId => Context.SharedReferences.SessionId;

    public override void GenerateCode()
    {
        Reg(IClientHandler);
        Reg(ClientHandler);
        Reg(UserId);
        Reg(SessionId);
        Code = @$"{GetNamespacesCode()}
// <auto-generated />
#nullable enable
namespace {Namespace};

public interface {Name}
{{
    {IClientHandler.Name} ToAll {{ get; }}
    {IClientHandler.Name} ToUser({UserId} userId);
    {IClientHandler.Name} ToSession({SessionId} userId);
}}";
    }
}