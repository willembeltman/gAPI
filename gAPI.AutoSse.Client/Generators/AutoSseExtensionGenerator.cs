using gAPI.AutoSseClient.Models;

namespace gAPI.AutoSseClient.Generators;

public class AutoSseExtensionGenerator : BaseGenerator
{
    public AutoSseExtensionGenerator(Generator context)
    {
        Context = context;

        Directory = "";
        Namespace = "gAPI.Generated";

        Name = "AutoSseClientExtension";
        FileName = $"{Name}.g.cs";
    }

    public Generator Context { get; }
    public SharedReference ClientConnection => Context.SseManager;
    public SharedReference IClientConnection => Context.SharedReferences.IClientConnection;
    public SharedReference SseManagerCollection => Context.SharedReferences.SseManagerCollection;

    public override void GenerateCode()
    {
        Reg("Microsoft.Extensions.DependencyInjection");
        Reg(ClientConnection);
        Reg(IClientConnection);
        Reg(SseManagerCollection);
        Code = $@"{GetNamespacesCode()}
// <auto-generated />
#nullable enable
namespace {Namespace};

public static class {Name}
{{
    public static IServiceCollection AddAutoSseClient(this IServiceCollection services)
    {{
        services.AddScoped<{ClientConnection}>();
        services.AddScoped<{IClientConnection}>(sp => sp.GetRequiredService<{ClientConnection}>());

        var sseManagerCollection = new {SseManagerCollection}();
        services.AddSingleton(sseManagerCollection);

        return services;
    }}
}}";
    }
}