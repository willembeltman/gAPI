using gAPI.AutoHub.Models;

namespace gAPI.AutoHub.Generators;

internal class SignalRHubGenerator : BaseGenerator
{
    internal SignalRHubGenerator(ServiceContext dataModel)
    {
        DataModel = dataModel;

        Directory = dataModel.Config.Hubs_Destination.Directory;
        Namespace = dataModel.Config.Hubs_Destination.Namespace;

        Name = "SignalRHub";
        FileName = $"{Name}.g.cs";
    }

    public ServiceContext DataModel { get; }

    public void GenerateCode()
    {
        Reg("Microsoft.AspNetCore.SignalR");
        Code = @$"{GetNamespacesCode()}
// <auto-generated />
#nullable enable
namespace {Namespace};

public class {Name}(gAPI.Interfaces.IServerAuthenticationService authenticationService) : Hub
{{
    List<string> UserIds = [];

    public override async Task OnConnectedAsync()
    {{
        //Console.WriteLine($""Client connected: {{Context.ConnectionId}}"");
        await base.OnConnectedAsync();
    }}

    public async Task InitializeAsync(Guid sessionId, CancellationToken ct)
    {{
        // PathString path, QueryString query, IPAddress? ipAddress, string? cookieData, StringValues sessionData, StringValues stateData, CancellationToken ct
        var httpContext = Context.GetHttpContext();

        await authenticationService.InitializeAsync(
            httpContext.Request.Path,
            httpContext.Request.QueryString,
            httpContext.Connection.RemoteIpAddress,
            httpContext.Request.Cookies[""AuthenticationToken""],
            httpContext.Request.Headers[""X-SessionId""],
            httpContext.Request.Headers[""X-StateData""],
            Context.ConnectionAborted
        );

        if (authenticationService.UserId == null) return;

        UserIds.Add(authenticationService.UserId);
        await Groups.AddToGroupAsync(Context.ConnectionId, authenticationService.UserId);
        //Console.WriteLine($""Client authenticated: {{Context.ConnectionId}} / {{authenticationService.UserId}}"");
    }}

    public override async Task OnDisconnectedAsync(Exception? exception)
    {{
        foreach (var userId in UserIds)
        {{
            await Groups.RemoveFromGroupAsync(Context.ConnectionId, userId);
            //Console.WriteLine($""Client removed from group: {{Context.ConnectionId}} / {{userId}}"");
        }}
        //Console.WriteLine($""Client disconnected: {{Context.ConnectionId}}"");
        await base.OnDisconnectedAsync(exception);
    }}
}}";
    }
}