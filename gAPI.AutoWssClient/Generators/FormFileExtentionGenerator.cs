namespace gAPI.AutoWssClient.Generators;

public class FormFileExtensionGenerator : BaseGenerator
{
    public FormFileExtensionGenerator(Generator context)
    {
        Context = context;

        if (context.SharedReferences.AuthClient_ToFormFileExtension != null)
        {
            Namespace = context.SharedReferences.AuthClient_ToFormFileExtension.Namespace;
            Name = context.SharedReferences.AuthClient_ToFormFileExtension.Name;
        }
        else
        {
            Namespace = "gAPI.Generated";
            Name = "FormFileExtension";
        }

        Directory = "";
        FileName = $"{Name}.g.cs";
    }

    public Generator Context { get; }

    public override void GenerateCode()
    {
        if (Context.SharedReferences.AuthClient_ToFormFileExtension != null)
            return;

        Code = $@"using gAPI.Attributes;
using Microsoft.AspNetCore.Components.Forms;
using System;
using System.IO;
using System.Threading.Tasks;

// <auto-generated />
#nullable enable
namespace {Namespace};

[IsFormFileExtension]
public static class IsFormFileExtension
{{
    public static async Task<FormFile> ToFormFileAsync(this IBrowserFile browserFile)
    {{
        if (browserFile.Size > int.MaxValue)
            throw new Exception(""File too big (4gb???!)"");
        var size = Convert.ToInt32(browserFile.Size);
        using var source = browserFile.OpenReadStream(5 * 1024 * 1024);
        using var ms = new MemoryStream(size);
        await source.CopyToAsync(ms);
        return new FormFile(browserFile, ms.ToArray());
    }}
}}";
    }
}
