using gAPI.AutoSseServer.Models;

namespace gAPI.AutoSseServer.Generators;

public class IClientServiceContext_Generator : BaseGenerator
{
    public IClientServiceContext_Generator(
        Generator context,
        ClientService_Generator sseService)
    {
        Context = context;
        SseService = sseService;

        Directory = "";
        Namespace = sseService.Namespace;

        Name = $"I{sseService.Interface.CleanName}Context";
        FileName = $"{Name}.g.cs";
    }

    public Generator Context { get; }
    public ClientService_Generator SseService { get; }

    public Interface Interface => SseService.Interface;

    public override void GenerateCode()
    {
        Reg(Interface);
        Reg(SseService);
        Code = @$"{GetNamespacesCode()}
// <auto-generated />
#nullable enable
namespace {Namespace};

public interface {Name}
{{
    {Interface.Name} ToAll {{ get; }}
    {Interface.Name} ToUser(string userId);
    {Interface.Name} ToSession(string sessionId);
}}
";
    }
}