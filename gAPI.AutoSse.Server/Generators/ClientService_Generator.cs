using gAPI.AutoSseServer.Models;
using System;
using System.Linq;

namespace gAPI.AutoSseServer.Generators;

public class ClientService_Generator : BaseGenerator
{
    public ClientService_Generator(Generator context, Interface @interface)
    {
        Context = context;
        Interface = @interface;

        Directory = "";
        Namespace = @interface.Namespace;

        Name = Interface.CleanName;
        FileName = $"{Name}.g.cs";
    }

    public Generator Context { get; }
    public Interface Interface { get; }
    public ServiceContext DataModel => Context.ServiceContext;

    public SharedReference ServiceMethodId => Context.SharedReferences.ServiceMethodId;
    public SharedReference FabricClient => Context.SharedReferences.FabricClient;
    public SharedReference UserId => Context.SharedReferences.UserId;
    public SharedReference SessionId => Context.SharedReferences.SessionId;
    public SharedReference ServiceId => Context.SharedReferences.ServiceId;

    public override void GenerateCode()
    {
        Reg("System.Text.Json");
        Reg(Interface);
        Reg(FabricClient);
        Reg(UserId);
        Reg(SessionId);
        Reg(ServiceId);
        Reg(ServiceId);
        Reg(ServiceMethodId);
        var methodsCode = GenerateMethods();


        Code = @$"// <auto-generated />
#nullable enable
namespace {Namespace};

public class {Name}(
    {FabricClient} FabricClient,
    {UserId}? UserId = null,
    {SessionId}? SessionId = null)
    : {Interface}
{{
    private readonly {ServiceId} ServiceId = new {ServiceId}(""{Interface}"");
{methodsCode}
{string.Join("\r\n", Interface.Methods.Select(m => @$"
    public class {Interface.Title}_{m.Name}
    {{{string.Join("", m.Arguments
        .Where(a => a.ParameterType.Name != "CancellationToken")
        .Select(p =>
        {
            RegRange(p.ParameterType.Namespaces);
            return @$"
        public {p.ParameterType.Name} {p.Name} {{ get; set; }}{(p.ParameterType.IsNullable || !p.ParameterType.IsReferenceType ? "" : " = null!;")}";
        }))}
    }}
"))}}}";
        Code = $"{GetNamespacesCode()}{Code}";
    }

    private string GenerateMethods()
    {
        var code = string.Empty;

        var first = true;
        foreach (var method in Interface.Methods)
        {
            if (first)
                first = false;
            else
                code += "\r\n";

            var hasFile = method.Arguments.Any(a => a.IsIFormFile);
            if (hasFile)
                throw new Exception(
                    $"`{method.ResponseType.Name} {Interface.Name}.{method.Name}` " +
                    "IFormFile is not supported by SignalR");

            var isTask = method.ResponseType.Name != "Void" || method.ResponseType.Name != "Task";
            if (isTask == false || method.ResponseType.UnderlayingTypes.Length > 0)
                throw new Exception(
                    $"`{method.ResponseType.Name} {Interface.Name}.{method.Name}` has a error: " +
                    "Only one-way calls (void / Task) are allowed, there is no return value, use the API directly for this");

            RegRange(method.Arguments.SelectMany(b => b.ParameterType.Namespaces));
            //var methodArguments = method.Arguments
            //        .Select(arg =>
            //        {
            //            RegRange(arg.ParameterType.Namespaces);
            //            return $"{arg.ParameterType.Name} {arg.Name}";
            //        })
            //        .ToList();

            //var methodSignature = string.Join(", ", methodArguments);
            //var methodCall = string.Join(", ", method.Arguments.Select(a => $"{a.Name}"));

            RegRange(method.ResponseType.Namespaces);

            var args = method.Arguments
                .Where(a => a.ParameterType.Name != "CancellationToken")
                .ToArray();
            var cancellationToken = method.Arguments
                .FirstOrDefault(a => a.ParameterType.Name == "CancellationToken");

            if (method.IsAsync)
            {
                if (method.ResponseType.Name != "Task")
                    throw new Exception(
                    $"`{method.ResponseType.Name} {Interface.Name}.{method.Name}` has a error: " +
                    "Only one-way calls (void / Task) are allowed, there is no return value, use the API directly for this");

                code += $@"
    public async Task {method.Name}(
        {string.Join(
            $",\r\n        ",
            method.Arguments.Select(a => @$"{a.ParameterType.Name} {a.Name}"))})
    {{
        var payload = new {Interface.Title}_{method.Name}
        {{
            {string.Join($",\r\n            ", args.Select(a => @$"{a.Name} = {a.Name}"))}
        }};
        var serviceMethodId = new {ServiceMethodId}(""{method.Name}"");
        var json = JsonSerializer.Serialize(payload);
        var bytes = System.Text.Encoding.UTF8.GetBytes(json);
        await FabricClient.SendAsync(ServiceId, serviceMethodId, UserId, SessionId, bytes{(cancellationToken == null ? ", default" : $", {cancellationToken.Name}")});
    }}";
            }
            else
            {
                if (method.ResponseType.Name != "Void")
                    throw new Exception(
                    $"`{method.ResponseType.Name} {Interface.Name}.{method.Name}` has a error: " +
                    "Only one-way calls (void / Task) are allowed, there is no return value, use the API directly for this");

                code += $@"
    public void {method.Name}({string.Join($",\r\n", method.Arguments.Select(a => @$"
            {a.ParameterType.Name} {a.Name}"))})
    {{
        var payload = new {Interface.Title}_{method.Name}
        {{{string.Join($",\r\n", args.Select(a => @$"
            {a.Name} = {a.Name}"))}
        }};
        var serviceMethodId = new {ServiceMethodId}(""{method.Name}"");
        var json = JsonSerializer.Serialize(payload);
        var bytes = System.Text.Encoding.UTF8.GetBytes(json);
        FabricClient.SendAsync(ServiceId, serviceMethodId, UserId, SessionId, bytes{(cancellationToken == null ? ", default" : $", {cancellationToken.Name}")}).GetAwaiter().GetResult();
    }}";
            }
        }


        return code;
    }
}