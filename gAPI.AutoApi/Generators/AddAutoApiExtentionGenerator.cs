
using gAPI.AutoApi.Models;

namespace gAPI.AutoApi.Generators;

internal class AddAutoApiExtentionGenerator : BaseGenerator
{
    internal AddAutoApiExtentionGenerator(ServiceContext serviceContext, AddAutoApiServicesExtentionGenerator addAutoApiServices)
    {
        ServiceContext = serviceContext;

        Directory = serviceContext.Config.AddAutoApiServices_Destination.Directory;
        Namespace = serviceContext.Config.AddAutoApiServices_Destination.Namespace;

        IServerAuthenticationService = serviceContext.IServerAuthenticationService;
        AddAutoApiServices = addAutoApiServices;

        Name = "AddAutoApiExtention";
        FileName = $"{Name}.g.cs";
    }

    public ServiceContext ServiceContext { get; }

    public SharedReference IServerAuthenticationService { get; }
    public AddAutoApiServicesExtentionGenerator AddAutoApiServices { get; }

    internal void GenerateCode()
    {
        Reg("Microsoft.AspNetCore.Authentication");
        Reg("Microsoft.AspNetCore.HttpOverrides");
        Reg("Microsoft.AspNetCore.Builder");
        Reg("Microsoft.AspNetCore.Mvc");
        Reg("System.Reflection");
        Reg(AddAutoApiServices);

        Code = $@"{GetNamespacesCode()}

// <auto-generated />
namespace {Namespace};

public static class {Name}
{{
    public static IServiceCollection AddAutoApi<TIServerAuthenticationService, TServerAuthenticationService, TAuthenticationHandler>(
        this IServiceCollection services, string frontendUrl, params Assembly[] assembliesToScan)
        where TIServerAuthenticationService : class, {IServerAuthenticationService.FullName}
        where TServerAuthenticationService : class, TIServerAuthenticationService 
        where TAuthenticationHandler : AuthenticationHandler<AuthenticationSchemeOptions>
    {{
        // JSON standaard op invariant zetten
        services.Configure<Microsoft.AspNetCore.Http.Json.JsonOptions>(options =>
        {{
            options.SerializerOptions.Encoder = System.Text.Encodings.Web.JavaScriptEncoder.UnsafeRelaxedJsonEscaping;
            options.SerializerOptions.PropertyNamingPolicy = null;
            options.SerializerOptions.WriteIndented = false;
        }});

        // Add normal asp.net core controllers
        services.AddControllers();

        // Http context accessor for gAPI
        services.AddHttpContextAccessor();

        // Remaining services
        services.AddAutoApiServices();

        // Add Cors
        services.AddCors(options =>
        {{
            options.AddPolicy(""AllowSpecificOrigin"", policy =>
            {{
                policy
                    .WithOrigins(frontendUrl)
                    .AllowAnyMethod()
                    .AllowAnyHeader()
                    .AllowCredentials()
                    .WithExposedHeaders(""X-SessionId"", ""X-StateData"");
            }});
        }});

        // Add gAPI server authentication 
        services.AddScoped<TServerAuthenticationService>();
        services.AddScoped<TIServerAuthenticationService>(sp => sp.GetRequiredService<TServerAuthenticationService>());
        services.AddScoped<{IServerAuthenticationService.FullName}>(sp => sp.GetRequiredService<TServerAuthenticationService>());
        services.AddAuthentication(""gAPI"")
                .AddScheme<AuthenticationSchemeOptions, TAuthenticationHandler>(""gAPI"", _ => {{ }});
        services.AddAuthorization();

        return services;
    }}

    public static WebApplication MapAutoApi<TMiddleware>(this WebApplication app)
    {{
        app.UseForwardedHeaders(new ForwardedHeadersOptions
        {{
            ForwardedHeaders = ForwardedHeaders.XForwardedFor | ForwardedHeaders.XForwardedProto
        }});

        app.UseCors(""AllowSpecificOrigin"");

        app.UseMiddleware<TMiddleware>();
        app.UseAuthentication();
        app.UseAuthorization();

        // 🔥 State handshake endpoint
        app.MapGet(""/__state"", ([FromHeader(Name = ""X-SessionId"")] string sessionId) => {{ }}).AllowAnonymous();

        app.MapControllers();

        return app;
    }}
}}

";

































